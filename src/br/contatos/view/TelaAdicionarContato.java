    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed
        //Verifica se os campos est√£o vazios
        String nome = jTextFieldNome.getText();
        String sobrenome = jTextFieldSobrenome.getText();
        if(nome.length() == 0) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha o campo \"Nome\"");
            jTextFieldNome.requestFocus();
            return;
        }
        if(sobrenome.length() == 0) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha o campo \"Sobrenome\"");
            jTextFieldSobrenome.requestFocus();
            return;
        }
        
        Contato c = new Contato(nome, sobrenome);
        
    }//GEN-LAST:event_jButtonAdicionarActionPerformed

